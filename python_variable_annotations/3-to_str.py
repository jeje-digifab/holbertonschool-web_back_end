#!/usr/bin/env python3
"""
This script performs various operations, such as converting floats to strings,
flooring values, and concatenating strings.

It is designed to be executed with Python 3.
"""


def to_str(n: float) -> str:
    """
    Converts a floating-point number to its string representation.

    Args:
        n (float): The floating-point number to convert.

    Returns:
        str: The string representation of the given floating-point number.
    """
    return str(n)
