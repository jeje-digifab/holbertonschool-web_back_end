#!/usr/bin/env python3
"""
This script performs various operations, such as converting floats to strings,
flooring values, and concatenating strings.

It is designed to be executed with Python 3.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings into a single string.

    Args:
        str1 (str): The first string to concatenate.
        str2 (str): The second string to concatenate.

    Returns:
        str: The resulting string after concatenation of `str1` and `str2`.
    """
    return (str1 + str2)
